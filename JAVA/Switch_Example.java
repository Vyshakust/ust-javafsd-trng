package com.ust;

public class Switch_Example {
	public static void main(String args[]) {
		String str="Green1";
		switch(str) {
		case "Red":
			System.out.println("im in red");
			System.out.println("*");
			break;
			
		default:
			System.out.println("this is default");
			break;
		
		case "Green":
			System.out.println("im in green");
			System.out.println("*");
			break;
		case "yellow":
			System.out.println("im in yellow");
			System.out.println("*");
			break;
			
		
			
			
			
		}
		
	}
}
