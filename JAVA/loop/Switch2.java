package Switch;

public class Switch2 {
	public static void main(String args[]) {
		int expression =9;
		
		switch(expression) {
		case 2:
			System.out.println("small size");
			break;
		
		case 3:
			System.out.println("large size");
			break;
			
		default:
			System.out.println("unknown size");
		}
	}
}
