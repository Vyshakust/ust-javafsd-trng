var grade:string="A";
switch(grade){
    case "A":{
        console.log("Excellent");
        break;
    }

    case "B":{
        console.log("Good");
        break;
    }

    case "c":{

        console.log("Fair");
        break;
    }

    case "D":{
        console.log("poor");
        break;
    }
    default:{

        console.log("invalid choice");
        break;
    }
}